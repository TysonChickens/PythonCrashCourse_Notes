# 4-1: List and loop favorite pizzas
favorite_pizzas = ['Pepperoni, Sausage, Bacon']

for pizza in favorite_pizzas:
    print(f"I like {pizza.lower()} pizza!")

print("I really don't eat much pizza. I am a simple man. I see some junk food - I eat it.\n")

# 4-2: List animals with common characteristics and then print them out using a for loop
animals = ['pandas, cheetahs, dogs, cats, kangaroos']
for animal in animals:
    print(f"All these animals look cute but can be dangerous. Beware of {animal}.")

print("I would love to have any of these animals as pets!")
